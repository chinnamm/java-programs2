package com;

public class stelseif {

	public static void main(String[] args) {
		String city="Delhi";
		if(city=="gadag")
		{
			System.out.println("city is gadag");
		}
		else if(city=="mangalore") {
			System.out.println("city is mangalore");
		}
		else if(city=="davanagere") {
			System.out.println("city is davanagere");
		}
		else
		{
			System.out.println("none of the above are true");
		}

	}

}
